# 정수 연산자
# +(더하기), -(빼기), *(곱하기), /(부동소수점 나누), //(정수 나누기(소수점이하 버림)), %(나머지), **(지수)

print("# 3장 연습문제")
print("# 3.1")
60 * 60
print("# 3.2")
seconds_per_hour = 60 * 60
seconds_per_hour
print("# 3.3")
seconds_per_day = seconds_per_hour * 24
seconds_per_day
print("# 3.4")
seconds_per_day / seconds_per_hour
print("# 3.5")
seconds_per_day // seconds_per_hour
